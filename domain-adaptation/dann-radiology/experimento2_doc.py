    # =========================
    sorted_p = np.sort(p)
    n = len(p)
    cum = np.cumsum(sorted_p)
    gini = (n + 1 - 2 * np.sum(cum) / cum[-1]) / n

    return {
        "mean_attention": mean_attention,
        "active_area": active_area,
        "entropy": entropy,
        "gini_concentration": gini
    }

