def evaluate(model, loader, device, name="Dataset"):
    model.eval()

    y_true = []
    y_pred = []

    with torch.no_grad():
        for x, y in loader:
            x = x.to(device)

            outputs, _ = model(x)
            probs = torch.softmax(outputs, dim=1)[:, 1]

            y_true.extend(y.numpy())
            y_pred.extend(probs.cpu().numpy())

    auc = roc_auc_score(y_true, y_pred)

    print(f"[{name}] AUC: {auc:.4f}")

    return auc

